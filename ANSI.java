public class ANSI {

    // Reset
    public static final String rst = "\u001B[0m";

    // Colors
    public static final String blk = "\u001B[30m"; // Black
    public static final String red = "\u001B[31m"; // Red
    public static final String grn = "\u001B[32m"; // Green
    public static final String yel = "\u001B[33m"; // Yellow
    public static final String blu = "\u001B[34m"; // Blue
    public static final String ppl = "\u001B[35m"; // Purple
    public static final String cyn = "\u001B[36m"; // Cyan
    public static final String wht = "\u001B[37m"; // White

    // Formatting
    public static final String bld = "\u001B[1m"; // Bold
    public static final String udl = "\u001B[4m"; // Underline
    
}